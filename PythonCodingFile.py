def apply_power_curve(wind_speed_series):
    """
    Converts predicted Wind Speed (m/s) into Active Power (kW)
    using the standard piecewise theoretical power curve.
    """
    wind_speed = np.array(wind_speed_series)
    power_output = np.zeros_like(wind_speed)

    for i, v in enumerate(wind_speed):
        if v < CUT_IN_SPEED:
            power_output[i] = 0
        elif v >= CUT_IN_SPEED and v < RATED_SPEED:
            # Cubic region: Power increases with cube of wind speed
            cp_ratio = ((v - CUT_IN_SPEED) / (RATED_SPEED - CUT_IN_SPEED))
            power_output[i] = RATED_POWER * (cp_ratio ** 3)
        elif v >= RATED_SPEED and v < CUT_OUT_SPEED:
            # Rated region: Constant max power
            power_output[i] = RATED_POWER
        else:
            # Cut-out region: Turbine stops for safety
            power_output[i] = 0

    return pd.Series(power_output, index=wind_speed_series.index)

