def optimize_holt_winters(train_wind_speed, val_wind_speed, val_power, period=144):
    """
    Optimize Holt-Winters on wind speed, then convert to power for evaluation.
    """
    print(f" > Grid Searching Holt-Winters (Period={period})...")
    best_rmse = float('inf')
    best_model = None
    best_config = None

    has_zeros = (train_wind_speed <= 0).any()
    trends = ['add', 'mul'] if not has_zeros else ['add']
    seasonals = ['add', 'mul'] if not has_zeros else ['add']
    damped_options = [True, False]

    for t in trends:
        for s in seasonals:
            for d in damped_options:
                try:
                    model = ExponentialSmoothing(
                        train_wind_speed,
                        seasonal_periods=period,
                        trend=t,
                        seasonal=s,
                        damped_trend=d
                    ).fit(optimized=True)

                    # Predict wind speed
                    wind_pred = model.forecast(len(val_wind_speed))
                    # Convert to power
                    power_pred = power_curve(wind_pred)
                    # Evaluate on power
                    rmse = np.sqrt(mean_squared_error(val_power, power_pred))

                    if rmse < best_rmse:
                        best_rmse = rmse
                        best_model = model
                        best_config = {'trend': t, 'seasonal': s, 'damped': d}
                except:
                    continue

    print(f"   Best HW Config: {best_config} | RMSE: {best_rmse:.4f}")
    return best_model, best_config

def optimize_arima(train_wind_speed, val_wind_speed, val_power):
    """
    Optimize ARIMA on wind speed using auto_arima.
    """
    print(" > Running Auto-ARIMA (this may take time)...")
    fit_subset = train_wind_speed.iloc[-10000:] if len(train_wind_speed) > 10000 else train_wind_speed

