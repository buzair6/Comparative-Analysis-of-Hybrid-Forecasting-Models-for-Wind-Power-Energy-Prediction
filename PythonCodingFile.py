# Helper to run Day-Ahead Loop
def run_day_ahead_forecast(model_name, train_data, test_data, horizon=144):
    """
    Simulates a daily operational forecast.
    1. Train on history.
    2. Forecast 24 hours (144 steps).
    3. Add actual 24 hours to history.
    4. Repeat.
    """
    history = train_data.copy()
    predictions = []

    # We iterate through the test set in chunks of 'horizon' (1 day)
    total_steps = len(test_data)

    for i in range(0, total_steps, horizon):
        # Define the forecast chunk size (handle the last partial chunk)
        steps_to_predict = min(horizon, total_steps - i)

        # --- MODEL SELECTION ---
        if model_name == 'HW':
            # Fast Holt-Winters: 144 period seasonality, no trend
            model = ExponentialSmoothing(
                history.iloc[-3000:], # Use last 3000 pts for speed
                seasonal_periods=144,
                trend=None,
                seasonal='add',
                initialization_method="heuristic"
            ).fit(optimized=True)
            pred_chunk = model.forecast(steps_to_predict)

        elif model_name == 'ARIMA':
            # Fast ARIMA: AR(2) - Simple Autoregressive
            model = ARIMA(history.iloc[-2000:], order=(2,0,0)).fit()
            pred_chunk = model.forecast(steps_to_predict)

        predictions.extend(pred_chunk)

        # UPDATE HISTORY: Add the *actual* data we just stepped over to the history
        # This is what resets the error and prevents drift!
        actual_chunk = test_data.iloc[i : i + steps_to_predict]
        history = pd.concat([history, actual_chunk])

        # Simple progress bar
        sys.stdout.write(f"\r  > {model_name} Progress: {min(100, int((i/total_steps)*100))}%")
        sys.stdout.flush()

    print(f"\r  > {model_name} Progress: 100% - Done.       ")
    return pd.Series(predictions, index=test_data.index)

