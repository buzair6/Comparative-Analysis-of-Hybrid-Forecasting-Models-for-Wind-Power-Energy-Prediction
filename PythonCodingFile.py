def create_sequences(data, seq_length):
    """
    Create sequences for LSTM training.
    """
    X, y = [], []
    for i in range(len(data) - seq_length):
        X.append(data[i:i + seq_length])
        y.append(data[i + seq_length])
    return np.array(X), np.array(y)

def build_lstm_model(seq_length, n_features=1):
    """
    Build LSTM model architecture.
    """
    model = Sequential([
        LSTM(128, activation='relu', return_sequences=True, input_shape=(seq_length, n_features)),
        Dropout(0.2),
        LSTM(64, activation='relu', return_sequences=True),
        Dropout(0.2),
        LSTM(32, activation='relu'),
        Dropout(0.2),
        Dense(16, activation='relu'),
        Dense(1)
    ])
    model.compile(optimizer='adam', loss='mse', metrics=['mae'])
    return model

def train_lstm_on_wind_speed(train_wind, val_wind, val_power, seq_length=144, epochs=50, batch_size=64):
    """
    Train LSTM on wind speed sequences, convert predictions to power.
    """
    print(" > Training LSTM on wind speed...")

    # Scale wind speed data
    scaler = MinMaxScaler()
    train_scaled = scaler.fit_transform(train_wind.values.reshape(-1, 1))
    val_scaled = scaler.transform(val_wind.values.reshape(-1, 1))

    # Create sequences
    X_train, y_train = create_sequences(train_scaled, seq_length)
    X_val, y_val = create_sequences(val_scaled, seq_length)

    # Build and train model
    model = build_lstm_model(seq_length)
    early_stop = EarlyStopping(monitor='val_loss', patience=5, restore_best_weights=True)

    history = model.fit(
        X_train, y_train,
        validation_data=(X_val, y_val),
        epochs=epochs,
        batch_size=batch_size,
        callbacks=[early_stop],
        verbose=0
    )

    # Generate predictions on validation set
    val_pred_scaled = model.predict(X_val, verbose=0)
    val_pred_wind = scaler.inverse_transform(val_pred_scaled).flatten()

    # Convert to power
    val_pred_power = power_curve(val_pred_wind)

    # Align with actual validation power (account for sequence loss)
    val_power_aligned = val_power.values[seq_length:]

    rmse = np.sqrt(mean_squared_error(val_power_aligned, val_pred_power))
    print(f"   LSTM RMSE: {rmse:.4f}")

    return model, scaler, seq_length

