def power_curve(wind_speed):
    """
    Convert wind speed to active power using a realistic power curve.
    Adjust parameters based on your turbine specifications.
    """
    wind_speed = np.array(wind_speed)
    power = np.zeros_like(wind_speed)

    # Typical wind turbine parameters
    cut_in = 3.0      # Cut-in wind speed (m/s)
    rated = 12.0      # Rated wind speed (m/s)
    cut_out = 25.0    # Cut-out wind speed (m/s)
    rated_power = 1500.0  # Rated power (kW) - adjust to your data

    # Power curve regions
    mask1 = (wind_speed >= cut_in) & (wind_speed < rated)
    mask2 = (wind_speed >= rated) & (wind_speed <= cut_out)

    # Cubic relationship in Region 1
    power[mask1] = rated_power * ((wind_speed[mask1] - cut_in) / (rated - cut_in)) ** 3

    # Constant rated power in Region 2
    power[mask2] = rated_power

    # Zero power outside operating range
    return power

